if __name__ == '__main__':
    # 命名法 - 驼峰命名法
    camelName = "I'm camelName variable"
    print("驼峰命名：" + camelName)

    # 命名法 - 帕斯卡命名
    PascalName = "I'm PascalName variable"
    print("帕斯卡命名：" + PascalName)

    # Python命名法 - 单前导下划线 私有变量或者方法
    _var = "I'm _single_leading_underscore"
    print("单前导命名：" + _var)

    # Python命名法 - 单末尾下划线 防止和系统级变量重名
    var_ = "I'm single_tail_underscore_"
    print("单末尾命名：" + var_)